public class Switch {
    public static void main(String[] args) {
        String name = "Ashwin";
        switch(name){
            case ("ash"):
                System.out.println("wrong");
                break;
            case ("win"):
                System.out.println("wrong");
                break;
            case ("Ashwin"):
                System.out.println("correct");
                break;
        }   
    }
}
